package day4;

public class VariablesPractice {
    public static void main(String[] args) {

        // int num7 = 100, num8 = 1000, num9 = 850000;

        byte num1 = 100;
        short num2 = 10000;
        double num3 = 3.5;
        float num4 = 2.5f;
        // double num4 = 2.5f; double larger then float and can be assigned bigger primitives
        long num5 = 999999999999l;
        int num6 = 850000;

        //float num7 = 5.5; larger primitives cannot be assigned to smaller primitives directly

        System.out.println("num1 =" +num1);
        System.out.println("num2 =" +num2);
        System.out.println("num3 =" +num3);
        System.out.println("num4 =" +num4);
        System.out.println("num5 =" +num5);
        System.out.println("num6 =" +num6);



    }
}
